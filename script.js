function acender() {
    document.querySelector('.acesa-img').style.display = 'block'
    document.querySelector('.apagada-img').style.display = 'none'
    document.body.style.backgroundColor = 'white'
    document.querySelector('.texto').style.color = 'black'

}

function apagar() {
    document.querySelector('.acesa-img').style.display = 'none'
    document.querySelector('.apagada-img').style.display = 'block'
    
}







